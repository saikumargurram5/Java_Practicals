package Basic;

public class Literals {
	
	/**
	 * 
	 * Literals are constant values assigned to constant variables. Literals represent fixed values that cannot 
	 * be modified. Literals are a synthetic representation of boolean, character, numeric, or string data,
	 *  a medium of expressing particular values in the program.
	 * 
	 */

	public static void main(String[] args) {

		int count = 987;  

		float floatVal = 4534.99f;  

		double cost = 19765.567;  

		int hexaVal = 0x7e4;  

		int binary = 0b11010;  

		char alpha = 'p';  

		String str = "Java";  

		boolean boolVal = true;  

		int octalVal = 067;  

		String stuName = null;  

		char ch1 = '\u0021';  

		char ch2 = 1456;  

		
		
		
		
System.out.println(count);  System.out.println(floatVal);  System.out.println(cost);  System.out.println(hexaVal);  		
System.out.println(binary);  System.out.println(alpha);  System.out.println(str);  System.out.println(boolVal);  
System.out.println(octalVal);  System.out.println(stuName);  System.out.println(ch1);  
System.out.println("\t" +"backslash literal");  System.out.println(ch2); 
		
	}

}
