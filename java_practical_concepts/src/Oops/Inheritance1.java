package Oops;

public class Inheritance1 extends Inheritance2{

	
	public void method5()
	{
		System.out.println(" This is method 5 ..............");
	}
	
	public void method6()
	{
		System.out.println(" This is method 6 ..............");
	}
	
	public void method7()
	{
		System.out.println(" This is method 7 ..............");
	}
	
	
	public static void main(String[] args) {
		
		
		Inheritance1 obj = new Inheritance1();
		obj.method1();
		obj.method2();
		obj.method3();
		obj.method4();
		obj.method5();
		obj.method7();
		
		
		
		
	}
}
